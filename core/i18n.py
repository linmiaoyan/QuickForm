def get_locale():
    """获取当前语言"""
    return session.get('locale', 'zh-simple')


def set_locale(locale):
    """设置当前语言"""
    if locale in ['zh-simple', 'zh-TW', 'en']:
        session['locale'] = locale
        return True
    return False


def translate(key, locale=None):
    """翻译文本"""
    if locale is None:
        locale = get_locale()
    
    translations = TRANSLATIONS.get(locale, TRANSLATIONS['zh-simple'])
    return translations.get(key, key)


def get_available_locales():
    """获取可用的语言列表"""
    return ['zh-simple', 'zh-TW', 'en']


def get_locale_name(locale):
    """获取语言显示名称"""
    names = {
        'zh-simple': '简体中文',
        'zh-TW': '繁體中文',
        'en': 'English'
    }
    return names.get(locale, locale)
